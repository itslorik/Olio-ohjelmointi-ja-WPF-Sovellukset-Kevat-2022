using System;
using System.Collections.Generic;
using System.Text;

namespace Laskin_Harjoitus
{
    static class Harjoitus5
    {
        public static float lasku(float A, float b)
        {
            return A  + b;
        }
        public static float Erotus(float A, float b)
        {
            return A - b;
        }
        public static float Kertolasku(float A, float b)
        {
            return A * b;
        }
        public static float Jakolasku(float A, float b)
        {
            return A / b;
        }
    }
}
